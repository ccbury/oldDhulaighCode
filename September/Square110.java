public class Square110 {

    public static void main (String args[]) {
    	
    	int squared;
    	
    	for(int i =1; i<=10; i++){
    		squared = i*i;
    		System.out.println(i+": "+ squared);
    	}
    	
    	
    }//End method
    
    
}//End class