public class ForLoop {

    public static void main (String args[]) {
    	
    	// Declare variables
    	int i;
    	
    	//Start the for loop
    	for (i=1; i<=10;i++){
    		System.out.println(i);
    	}//End for
    	
    	
    }// End main
    
    
}// End class