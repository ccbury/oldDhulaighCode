public class PrintingOnDifferentLines {

    public static void main(String args[]) {
    	
    	System.out.println("Conor Bury");
    	System.out.print("Conor");
    	System.out.println("Bury");
    	
    	// \n moves the cursor to a new line
    	System.out.println("Conor\nBury");
    	
    	//\t moves the cursor foreward one tab
    	System.out.println("Conor\tBury");
    	
    	
    }//End of main method
    
    
}//End of class