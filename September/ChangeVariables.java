public class ChangeVariables {

    public static void main (String args[]) {
    	
    	//Declare variables
    	String name = "Conor";
    	int age = 19;
    	
    	//Print the original values
    	System.out.println("My name is " + name);
    	System.out.println("I am " + age + " year(s) old");
    	System.out.println();
    	
    	//Change the variable values
    	name = "Karl";
    	age = 18;
    	
    	//Print the new values
    	System.out.println("My new name is " + name);
    	System.out.println("My new age is " + age);
    	System.out.println();
    		
    }//End of main mathod
    
    
}//End of class